n, m = len(maze), len(maze[0])
cell = 20
root = tk.Tk()
w, h = m * cell, n * cell         
root.update_idletasks()
sw, sh = root.winfo_screenwidth(), root.winfo_screenheight()
x = (sw - w) // 2
y = (sh - h) // 2
root.geometry(f"{w}x{h}+{x}+{y}")
c = tk.Canvas(root, width=w, height=h)
c.pack()

start_i = start_j = None
visited = []
path = []  
flag = False

def find_start():
    global start_i, start_j
    for i in range(n):
        for j in range(m):
            if maze[i][j] == 'S':
                start_i, start_j = i, j
                return True
    return False

def draw():
    c.delete("all")
    for i in range(n):
        for j in range(m):
            v = maze[i][j]
            if v == '0':
                color = "black"
            elif v == 'E':
                color = "lime" if (i, j) in path else "red"  # Зеленый если в пути
            else:
                color = "blue" if (i, j) in path else ("blue" if (i, j) == (start_i, start_j) else "white")
            
            x1, y1 = j * cell, i * cell
            c.create_rectangle(x1, y1, x1 + cell, y1 + cell, fill=color, outline="gray")

# НОВЫЙ АЛГОРИТМ BFS - ищет КРАТЧАЙШИЙ путь!
from collections import deque

def bfs(start_i, start_j):
    global flag, path, visited
    queue = deque([(start_i, start_j, [])])  # (i, j, путь_до_этого)
    visited = [(start_i, start_j)]
    
    while queue:
        i, j, current_path = queue.popleft()
        
        # Анимация
        path = current_path + [(i, j)]
        draw()
        root.update()
        root.after(50)  # Плавная задержка
        
        if maze[i][j] == 'E':
            path = current_path + [(i, j)]  # Финальный путь
            flag = True
            return True
        
        # Соседи в порядке: вверх, вниз, лево, право (можно менять приоритет)
        for di, dj in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
            ni, nj = i + di, j + dj
            if (0 <= ni < n and 0 <= nj < m and 
                maze[ni][nj] != '0' and (ni, nj) not in visited):
                
                visited.append((ni, nj))
                queue.append((ni, nj, current_path + [(i, j)]))
    
    return False

if find_start():
    draw()
    print("Ищем кратчайший путь (BFS)...")
    found_exits = bfs(start_i, start_j)
    if found_exits:
        print("✓ Кратчайший путь найден!")
        print(f"Длина пути: {len(path)} шагов")
        draw()  # Финальная отрисовка
    else:
        print("✗ Выход недостижим")

